# Operasi Logical
# or, and, not

var3 = False
var4 = True

print("operasi AND===")
print(int(not(var3) and var4))
print(int(var3 and var4))
print(int(not(var3) and not(var4)))
print(int(var3 and not(var4)))
print("======")

print("operasi OR =====")
print(int(not(var3) or var4))
print(int(var3 or var4))
print(int(not(var3) or not(var4)))
print(int(var3 or not(var4)))